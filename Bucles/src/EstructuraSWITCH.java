
public class EstructuraSWITCH {

	public static void main (String[] args) {
 
		int num = 2;
 
		switch(num){
			
			case 1:
				System.out.println("Es un uno");
			break;
			
			case 2:
				System.out.println("Es un dos");
			break;
			
			case 3:
				System.out.println("Es un tres");
			break;
		
			default:
				System.out.println("Es un numero mayor que tres");
			break;
			
		}
   	}
}